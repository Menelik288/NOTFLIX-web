export const NotFlixData = {
    getAll: () => notflixCatalog,
    getById: (id) => notflixCatalog.find(item => item.id === id),
    getMovies: () => notflixCatalog.filter(item => item.type === "movie"),
    getTVShows: () => notflixCatalog.filter(item => item.type === "tv"),
    getByGenre: (genre) => notflixCatalog.filter(item => item.genres.includes(genre)),
    getGenresList: () => {
        const genres = new Set();
        notflixCatalog.forEach(item => item.genres.forEach(g => genres.add(g)));
        return Array.from(genres);
    },
    search: (query) => {
        const q = query.toLowerCase().trim();
        if (!q) return [];
        return notflixCatalog.filter(item => 
            item.title.toLowerCase().includes(q) || 
            item.overview.toLowerCase().includes(q) || 
            item.genres.some(g => g.toLowerCase().includes(q)) ||
            item.cast.some(c => c.name.toLowerCase().includes(q))
        );
    }
};
